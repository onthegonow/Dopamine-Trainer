import SwiftUI

struct EmojiPickerSheet: View {
    var onSelect: (String, String) -> Void
    var onCancel: () -> Void

    @State private var selectedEmoji: String?
    @State private var label: String = ""
    @FocusState private var isLabelFocused: Bool

    private let columns = [GridItem(.adaptive(minimum: 36), spacing: 8)]
    
    private var isLabelValid: Bool {
        !label.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty
    }

    var body: some View {
        VStack(alignment: .leading, spacing: 12) {
            Text("Add a new craving")
                .font(.headline)

            HStack(spacing: 12) {
                Text(selectedEmoji ?? "ğŸ™‚")
                    .font(.system(size: 32))
                    .frame(width: 44, height: 44)
                    .background(Color.secondary.opacity(0.1))
                    .cornerRadius(8)
                
                TextField("Name your craving", text: $label)
                    .textFieldStyle(.roundedBorder)
                    .focused($isLabelFocused)
            }

            ScrollView {
                LazyVGrid(columns: columns, spacing: 8) {
                    // Default tags section
                    ForEach(UrgeTag.defaultCatalog, id: \.emoji) { tag in
                        Button {
                            selectedEmoji = tag.emoji
                            // Auto-fill label with the tag's label if the field is empty
                            if label.isEmpty {
                                label = tag.label
                            }
                        } label: {
                            Text(tag.emoji)
                                .font(.title2)
                                .frame(width: 36, height: 36)
                                .background(
                                    RoundedRectangle(cornerRadius: 6)
                                        .fill(selectedEmoji == tag.emoji ? Color.accentColor.opacity(0.2) : Color.clear)
                                )
                        }
                        .buttonStyle(.plain)
                        .help(tag.label)
                    }
                }
                .padding(.bottom, 8)
                
                // Thin divider line
                Divider()
                    .padding(.bottom, 8)
                
                // All other emojis
                LazyVGrid(columns: columns, spacing: 8) {
                    ForEach(EmojiPickerData.commonEmojis, id: \.self) { e in
                        Button {
                            selectedEmoji = e
                        } label: {
                            Text(e)
                                .font(.title2)
                                .frame(width: 36, height: 36)
                                .background(
                                    RoundedRectangle(cornerRadius: 6)
                                        .fill(selectedEmoji == e ? Color.accentColor.opacity(0.2) : Color.clear)
                                )
                        }
                        .buttonStyle(.plain)
                    }
                }
            }
            .frame(height: 240)
            
            if !isLabelValid && !label.isEmpty {
                Text("Craving name is required")
                    .font(.caption)
                    .foregroundColor(.red)
            }

            HStack {
                Spacer()
                Button("Cancel") { onCancel() }
                Button("Add") {
                    if let e = selectedEmoji, isLabelValid {
                        onSelect(e, label.trimmingCharacters(in: .whitespacesAndNewlines))
                    }
                }
                .disabled(selectedEmoji == nil || !isLabelValid)
                .keyboardShortcut(.defaultAction)
            }
        }
        .padding()
        .frame(width: 420, height: 440)
        .onAppear {
            isLabelFocused = true // Focus the label field when sheet appears
        }
    }
}

